# Crie um algoritimo que leia um número e mostre seu dobro, tríplo e raiz quadrada

numero = float(input('Digite um número: '))
dobro = numero * 2
tripulo = numero * 3
raiz_quadrada = numero ** (1/2)

print(f'Você digitou o número {numero}!\nO seu dobro é {dobro} \nTrípulo é {tripulo} \nRaiz quadrada: {raiz_quadrada}')
print('======= FIM ========')