#OPERADORES MATEMÁTICO
faturamento = 1000
custo = 300
novas_vendas = 100

faturamento = faturamento + novas_vendas # Adição
taxa_imposto = 0.1
imposto = faturamento * taxa_imposto # Multiplicação
lucro = faturamento - custo - imposto # Subtração
margem_lucro = lucro / faturamento # Divisão
