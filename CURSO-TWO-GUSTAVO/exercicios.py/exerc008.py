# Escreva um programaque leia um valor em metros e o exiba convertido em centimetro e minlimetro

metro = float(input('Informe o metro: '))
centimetro = metro * 100
minlimetro = metro * 1000

print(f'Você digitou {metro} metros! \nEm centimetro é {centimetro} cm e em milimetro é {minlimetro} mm')
